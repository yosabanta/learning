#importing the 'math' module
import math
#taking input from the user
num = int(input("Enter a number: "))

square_root = math.sqrt(num)#Calculation od square root
logarithm = math.log(num)#Calculation of logarithm
sine = math.sin(num)#Calculation of sin value
#Final results
print(f'square root:{square_root}')
print(f'logarithm: {logarithm}')
print(f'sine: {sine}')